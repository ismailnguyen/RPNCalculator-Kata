namespace RPNCalculator_Kata
{
    public class RPNOperator
    {
        public static int Sum(int x, int y)
        {
            return x + y;
        }

        public static int Substract(int x, int y)
        {
            return x - y;
        }

        public static int Divide(int x, int y)
        {
            return x / y;
        }

        public static int Multiply(int x, int y)
        {
            return x * y;
        }
    }
}
